    private void jBAceptarUsuariosCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAceptarUsuariosCrearActionPerformed
        // TODO add your handling code here:
        
        PreparedStatement consulta;
        ResultSet resultado;
        
        String nombreUsuario = jTTNombreUsuarioCrear.getText();
        String apellidoUsuario = jTFApellidoUsuarioCrear.getText();
        String direccionUsuario = jTFDireccionUsuarioCrear.getText();
        String telefonoUsuario = jTFTelefonoUsuarioCrear.getText();
        String cargoUsuario = jTFCargoUsuarioCrear.getText();
        String usuario= jTFUsuarioUsuarioCrear.getText();
        String contrasenia = jTFContraseniaaUsuarioCrear.getText();
        String correo = jTFCorreoUsuarioCrear.getText();
        String cui = jTFCuiUsuarioCrear.getText();
        
        
        rolusuario rol = (rolusuario) jCBRolUsuario.getSelectedItem();
        int rolseleccionado=  rol.getIdrol();
        
        //VALLIDACION
        //********************************************************************************
        if (nombreUsuario.trim().equals("")){
             JOptionPane.showMessageDialog(null, "Debe ingresar el nombre del usuario ","ERROR",2);
            
        }else if (apellidoUsuario.trim().equals("")){
            
             JOptionPane.showMessageDialog(null, "Debe ingresar el apellido del usuario ","ERROR",2);
        } else if (direccionUsuario.trim().equals("")){
              JOptionPane.showMessageDialog(null, "Debe ingresar la direccion del usuario ","ERROR",2);
            
        }else if (telefonoUsuario.trim().equals("")){
              JOptionPane.showMessageDialog(null, "Debe ingresar el telefono del usuario ","ERROR",2);
            
        } else if (cargoUsuario.trim().equals("")){
            
              JOptionPane.showMessageDialog(null, "Debe ingresar el cargo del usuario ","ERROR",2);
        }else if (usuario.trim().equals("")){
              JOptionPane.showMessageDialog(null, "Debe ingresar un nombre de usuario ","ERROR",2);
        }//agregar mas validaciones a la contrase√±a
        else if (contrasenia.trim().equals("")){
              JOptionPane.showMessageDialog(null, "Debe ingresar una contrase√±a ","ERROR",2);
        }//agragar mas validaciones al correo
        else if (correo.trim().equals("")){
              JOptionPane.showMessageDialog(null, "Debe ingresar un correo valido","ERROR",2);
        }else if (cui.trim().equals("")){
              JOptionPane.showMessageDialog(null, "Debe ingresar el CUI del usuario ","ERROR",2);
        }
        //**********************************************************************************************
        else {
            try {
              String query="INSERT INTO usuario (nombre,apellido,direccion,telefono,cargo,usuario,contrasenia,correoelectronico,cui,rol_idrol) "
                      + "VALUES ('"+nombreUsuario+"','"+apellidoUsuario+"','"+direccionUsuario+"','"+telefonoUsuario+"','"+cargoUsuario+"','"+usuario+"','"+contrasenia+"','"+correo+"','"+cui+"','"+rolseleccionado+"')";
                
             consulta = conexion.getConection().prepareStatement(query);
             consulta.executeUpdate();
             
            jTTNombreUsuarioCrear.setText("");
            jTFApellidoUsuarioCrear.setText("");
            jTFDireccionUsuarioCrear.setText("");
            jTFTelefonoUsuarioCrear.setText("");
            jTFCargoUsuarioCrear.setText("");
            jTFUsuarioUsuarioCrear.setText("");
            jTFContraseniaaUsuarioCrear.setText("");
            jTFCorreoUsuarioCrear.setText("");
            jTFCuiUsuarioCrear.setText("");
             
              
            }catch(SQLException ex){
                System.out.println(ex.toString());
            }
            
        }
        
        
    }//GEN-LAST:event_jBAceptarUsuariosCrearActionPerformed

