    private void jBAceptarProductoCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAceptarProductoCrearActionPerformed
        // TODO add your handling code here:
        
        PreparedStatement consulta;
        ResultSet resultado;
        
        String nombreProducto = jTFNombreProductoCrear.getText();
        String descripcionProducto= jTFDescripcionProductoCrear.getText();

       categoria cat = (categoria) jCBCategoriaProductoAgregar.getSelectedItem();
       int categoria=  cat.getIdcategoria();
       
      
     
        //VALIDACION
        ///******************************************************
        if ((nombreProducto.trim().equals(""))){
            JOptionPane.showMessageDialog(null, "Debe ingresar un nombre del producto ","ERROR",2);
        }else if((descripcionProducto.trim().equals(""))){
             JOptionPane.showMessageDialog(null, "Debe ingresar una descripcion del producto ","ERROR",2);
        } else if(jTFPrecioCostoCrear.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Debe ingresar un  precio costo del producto ","ERROR",2);
        } else if (jTFPrecioVentaCrear.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Debe ingresar un  precio venta del producto ","ERROR",2);
        } else if (jTFExistenciaCrear.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Debe ingresar la existencia del producto ","ERROR",2);
        } else if((categoria==0)){
             JOptionPane.showMessageDialog(null, "Debe ingresar una categoria del producto ","ERROR",2);
        }
        else if (categoria==0){
            JOptionPane.showMessageDialog(null, "Debe seleccionar una categoria ","ERROR",2);
        }
        //***************************************************************************
         else{ 
            float costoProducto = Float.parseFloat(jTFPrecioCostoCrear.getText());
            float costoVentaProducto= Float.parseFloat(jTFPrecioVentaCrear.getText());
            int existenciaProducto= Integer.parseInt(jTFExistenciaCrear.getText());
            
        try{
            
            String query= "INSERT INTO producto (nombre,descripcion,preciocosto,precioventa,existencia,categoria_idcategoria )"
                    + "VALUES('"+nombreProducto+"','"+descripcionProducto+"','"+costoProducto+"','"+costoVentaProducto+"','"+existenciaProducto+"','"+categoria+"')";
            
            consulta=conexion.getConection().prepareStatement(query);
            consulta.executeUpdate();
            
            jTFNombreProductoCrear.setText("");
            jTFDescripcionProductoCrear.setText("");
            jTFPrecioCostoCrear.setText("");
            jTFPrecioVentaCrear.setText("");
            jTFExistenciaCrear.setText("");
            
            
        } catch(SQLException ex){
            Logger.getLogger(JPLogin.class.getName()).log(Level.SEVERE,null,ex);
        }
        }
        
   
  
     
      
    }//GEN-LAST:event_jBAceptarProductoCrearActionPerformed

